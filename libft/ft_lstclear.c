#include <stdlib.h>
#include "libft.h"

void	ft_lstclear(t_list **lst, void (*del)(void *))
{
	t_list	*current;
	t_list	*prev;

	if (!lst)
		return ;
	current = *lst;
	prev = current;
	while (current)
	{
		if (current->data)
			del(current->data);
		current = current->next;
		free(prev);
		prev = current;
	}
	*lst = NULL;
}
